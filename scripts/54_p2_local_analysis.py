    print("J[2] = (Z/2)^4, so max possible rank = 4")
    print()

    all_rank_4 = True
    for r in range(2, 101):
        rank = compute_local_rank_at_2(r)
        if rank != 4:
            print(f"r = {r}: LOCAL rank at p=2 = {rank}  ***")
            all_rank_4 = False
        elif r <= 20 or r % 25 == 0:
            print(f"r = {r:3d}: LOCAL rank at p=2 = {rank}")

    if all_rank_4:
        print("\n*** ALL r=2..100: local rank at p=2 = 4 ***")
        print("=> delta(J[2]) injects into the LOCAL Kummer image at p=2")

    # Part 3: Compare global vs local dimensions
    print("\n" + "=" * 70)
